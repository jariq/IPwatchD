/** \file message.c
 * \brief Contains logic used for manipulating program messages
 */


#include "ipwatchd.h"


extern int debug_flag;
extern int syslog_flag;


//! Handles output of messages generated by IPwatchD
/*!
 * \param msg Message to output
 * \param type Type of message. Possible values are IPWD_MSG_INFO, IPWD_MSG_ERROR and IPWD_MSG_ALERT.
 */
void ipwd_message (char *msg, int type)
{

	/* In daemon mode - must record messages by syslog */
	if (syslog_flag)
	{

		switch (type)
		{

			case IPWD_MSG_INFO:
				syslog (LOG_INFO, msg);
				break;

			case IPWD_MSG_ERROR:
				syslog (LOG_ERR, msg);
				break;

			case IPWD_MSG_ALERT:
				syslog (LOG_ALERT, msg);
				break;

			case IPWD_MSG_DEBUG:
				if (debug_flag)
				{
					syslog (LOG_DEBUG, msg);
				}
				break;

			default:
				syslog (LOG_ERR, msg);
				break;

		}

	}
	else
	/* Not daemon yet - messages can be printed to terminal */
	{

		switch (type)
		{

			case IPWD_MSG_INFO:
				fprintf (stdout, "%s\n", msg);
				break;

			case IPWD_MSG_ERROR:
				fprintf (stderr, "%s\n", msg);
				break;

			case IPWD_MSG_ALERT:
				fprintf (stderr, "%s\n", msg);
				break;

			case IPWD_MSG_DEBUG:
				if (debug_flag)
				{
					fprintf (stdout, "%s\n", msg);
				}
				break;

			default:
				fprintf (stderr, "%s\n", msg);
				break;

		}

	}

}

